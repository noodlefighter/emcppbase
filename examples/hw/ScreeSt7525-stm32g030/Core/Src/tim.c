static uint32_t s_TimerCount = 0;

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == htim14.Instance)
    {
        /* Toggle LED */

		s_TimerCount++;
    }

}

uint32_t GetSystem1msCount(void)
{
	return s_TimerCount;
}

uint32_t CalculateTime( uint32_t nNew, uint32_t nOld)
{
	uint32_t nTemp = 0;
	if (nNew >= nOld)
	{
		nTemp = nNew - nOld;
	}
	else
	{
		nTemp = 0xFFFFFFFF - nOld + nNew;
	}
	return (nTemp);
}

void delay_us(uint32_t nus)	  //量程0-8191us
{
	__HAL_TIM_SetCounter(&htim16, 0);//htim8

	__HAL_TIM_ENABLE(&htim16);

		if(nus > 8191)
		{
			nus = 8191;
		}
	while(__HAL_TIM_GetCounter(&htim16) < (8*nus));//计数频率8MHz,8次即为1us
	/* Disable the Peripheral */
	__HAL_TIM_DISABLE(&htim16);
}

